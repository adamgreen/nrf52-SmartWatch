// CONSTRUCTORS ------------------------------------------------------------
/*!
    @brief   Adafruit_SPITFT constructor for hardware SPI using a specific
             SPI peripheral.
    @param   width     Display width in pixels at default rotation (0).
    @param   height    Display height in pixels at default rotation (0).
    @param   pSpi      Pointer to nRF SPI object.
    @param   mosiPin   Pin # for MOSI.
    @param   sckPin    Pin # for SCLK.
    @param   cs        Pin # for chip-select (-1 if unused, tie CS low).
    @param   dc        Pin # for data/command select (required).
    @param   rst       Pin # for display reset (optional, display reset
                       can be tied to MCU reset, default of -1 means unused).
    @note    Output pins are not initialized in constructor; application
             typically will need to call subclass' begin() function, which
             in turn calls this library's initSPI() function to initialize
             pins.
*/
Adafruit_SPITFT::Adafruit_SPITFT(uint16_t width, uint16_t height, nrf_drv_spi_t* pSpi,
                                 uint8_t mosiPin, uint8_t sckPin, uint8_t csPin, uint8_t dcPin,
                                 uint8_t rstPin /* = NRF_DRV_SPI_PIN_NOT_USED */)
    : Adafruit_GFX(width, height), m_pSpi(pSpi), m_mosiPin(mosiPin), m_sckPin(sckPin), m_rstPin(rstPin),
      m_csPin(csPin), m_dcPin(dcPin), m_startCount(0), m_finCount(0)
{
    g_pSingleton = this;
}
